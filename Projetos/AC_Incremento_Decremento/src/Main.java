public class Main {
    public static void main(String[] args) {

        int numero = 10;

        // Incrementar
        numero += 1;
        System.out.println(numero);
        System.out.println();

        // Incrementar
        numero++;
        System.out.println(numero);
        System.out.println();

        // Incremento com FOR
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        System.out.println();

        // Decremento com FOR
        for(int i = 10; i >= 0; i--){
            System.out.println(i);
        }

    }
}
